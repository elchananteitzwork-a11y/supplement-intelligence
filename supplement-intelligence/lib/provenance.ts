// ── Fields whose provenance depends on this specific memo's signal_metadata —
// real external data was available for some generations and not others. ──

export function demandProvenance(sig?: SignalMetadata): Provenance {
  if (sig?.demand_verified) {
    return estimated(
      'Keepa (Amazon)',
      'Real Amazon sales-rank / search-volume data was retrieved and inserted into the prompt as required grounding context, with an explicit instruction not to override it. The model still wrote the final score and notes text — compliance is a prompt instruction, not a server-enforced calculation.'
    )
  }
  return synthesized('No external demand data was available for this query — score and notes are the model\'s own estimate from training knowledge.')
}

export function viralityProvenance(sig?: SignalMetadata): Provenance {
  if (sig?.virality_verified) {
    return estimated(
      'TikTok signal scrape',
      'Real TikTok content-volume/engagement signal was retrieved and inserted into the prompt as grounding context. The model still wrote the final score and notes text itself.'
    )
  }
  return synthesized('No external virality data was available for this query — score and notes are the model\'s own estimate.')
}

export function subscriptionProvenance(): Provenance {
  return synthesized(
    'Subscription-fit reasoning (repurchase cycle, retention) is the model\'s own judgment — no real repeat-purchase data source exists for a product that has not launched yet.'
  )
}

