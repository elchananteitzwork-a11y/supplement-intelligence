// ── Fields whose provenance depends on this specific memo's signal_metadata —
// real external data was available for some generations and not others. ──

export function demandProvenance(sig?: SignalMetadata): Provenance {
  if (sig?.demand_verified) {
    return estimated(
      'Keepa (Amazon)',
      'Real Amazon sales-rank / search-volume data was retrieved and inserted into the prompt as required grounding context, with an explicit instruction not to override it. The model still wrote the final score and notes text — compliance is a prompt instruction, not a server-enforced calculation.'
    )
  }
  return synthesized('No external demand data was available for this query — score and notes are the model\'s own estimate from training knowledge.')
}

export function viralityProvenance(sig?: SignalMetadata): Provenance {
  if (sig?.virality_verified) {
    return estimated(
      'TikTok signal scrape',
      'Real TikTok content-volume/engagement signal was retrieved and inserted into the prompt as grounding context. The model still wrote the final score and notes text itself.'
    )
  }
  return synthesized('No external virality data was available for this query — score and notes are the model\'s own estimate.')
}

export function subscriptionProvenance(): Provenance {
  return synthesized(
    'Subscription-fit reasoning (repurchase cycle, retention) is the model\'s own judgment — no real repeat-purchase data source exists for a product that has not launched yet.'
  )
}

export function manufacturingScoreProvenance(): Provenance {
  return synthesized(
    "Formulation-complexity judgment by the model. For sourced cost/MOQ/supplier figures, see the Manufacturing Intelligence tab below."
  )
}

export function marketSaturationProvenance(sig?: SignalMetadata): Provenance {
  if (sig?.market_verified) {
    return estimated(
      'Amazon seller-data signal',
      'Real competing-seller-count and saturation signal from Amazon listing data was inserted into the prompt as grounding context. The model still wrote the maturity/concentration/competitive-intensity text itself.'
    )
  }
  return synthesized('No external competitive-density data was available for this query — concentration, entry difficulty, and named dominant brands are the model\'s own recall.')
}

// "Biggest Competitor" — name and revenue are real when a competitor ASIN
// was found (Apify) and a targeted Keepa lookup on that exact ASIN
// succeeded (real price × real monthlySold). .gap stays model-written — a
// qualitative judgment, not a fetchable fact — though the model is now
// instructed to cite real Consumer Intelligence themes for it when available.
export function biggestCompetitorProvenance(sig?: SignalMetadata): Provenance {
  if (sig?.competitor_revenue_verified) {
    return verified(
      'Apify + Keepa',
      "Name is the real #1 competitor by review count for this exact query (Apify). Revenue is a targeted Keepa lookup on that exact ASIN — real price × Keepa's own monthlySold estimate for that specific product, not a category average and not a guess. The gap sentence is still model-written."
    )
  }
  return synthesized("Competitor name, revenue, and gap are the model's best-guess recall from training knowledge — not pulled from Crunchbase, SEC filings, or any company database. (Real grounding requires a competitor ASIN to be found first; none was available for this query.)")
}

// ── Manufacturing Intelligence tab — a separate pipeline (/api/manufacturing)
// from the main memo body, with its own real-data path. ──

export function manufacturingTabProvenance(dataSource: string | undefined): Provenance {
  if (dataSource && dataSource !== 'ai_synthesis') {
    return verified(
      'Apify (live Alibaba supplier listings)',
      'Unit cost, MOQ, and supplier count are aggregated directly from real scraped Alibaba supplier listings — currency-converted, not generated by the model. Reflects listed asking prices, not a negotiated quote.'
    )
  }
  return synthesized('No live supplier listings were available — figures are the model\'s category-benchmark estimate.')
}

