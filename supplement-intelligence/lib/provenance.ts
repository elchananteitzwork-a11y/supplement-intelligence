// ── False-precision guard: a bare model-generated percentage implies a
// calibrated statistical probability that doesn't exist here. Round to a
// 10-point band so the displayed number stops overstating its own
// precision, without inventing new bounds that aren't in the source value. ──

export function toConfidenceBand(pctStr: string | undefined): string {
  if (!pctStr) return pctStr ?? 'N/A'
  const match = pctStr.match(/(\d+(?:\.\d+)?)/)
  if (!match) return pctStr
  const n = parseFloat(match[1])
  const lo = Math.floor(n / 10) * 10
  const hi = lo + 10
  return `${lo}–${hi}%`
}

// ═══════════════════════════════════════════════════════════════
// EVIDENCE-FIRST LAYER — provenance for the raw signal/keyword data
// captured server-side (memo.signal_evidence / memo.keyword_intelligence).
// This data bypasses the LLM entirely, so classification is based on the
// underlying source's own nature (measured fact vs. provider-side estimate
// vs. a transparent formula over real inputs) — never "did the model use
// it correctly," since the model never touches this layer at all.
// ═══════════════════════════════════════════════════════════════

// ── No-fabrication zone: Search Volume, Revenue, Keyword Volume, and
// Competitor Counts. For these four specifically, there is no "fall back to
// the model's judgment" path — if the real source isn't there, the function
// returns `null` and the caller renders "No data available" literally,
// per explicit instruction. Every other provenance function in this file
// still degrades to `synthesized(...)` because a model-judgment fallback is
// fine for things that are inherently judgment calls (a thesis sentence, a
// risk severity); it is not fine for a number presented as a fact. ──

// "Monthly Search Volume" — real only via DataForSEO (m.keyword_intelligence).
// Neither Keepa nor Google Trends can measure this (see DemandSignal.search_volume
// comment in lib/signal-engine/types.ts) and no longer try to.
export function searchVolumeProvenance(ki?: KeywordIntelligence | null): Provenance | null {
  if (!ki || ki.top_buying.length === 0) return null
  return verified('DataForSEO', "Real monthly search volume for this query's top keyword, pulled directly from DataForSEO's keyword database.")
}

// "Search Growth %" — real via Google Trends' period-over-period computation
// on real (if relative) interest data. This is arithmetic on real numbers,
// not a guess, so it's allowed to exist even without DataForSEO.
export function searchGrowthProvenance(signals?: AggregatedSignals | null): Provenance | null {
  if (!signals?.growth) return null
  return estimated(
    signals.growth.primarySource,
    `Computed from real period-over-period comparison of ${signals.growth.primarySource} time-series data — the direction and magnitude are real arithmetic on real data points, not a model guess.`
  )
}

// "Estimated Monthly Units Sold" — real via Keepa's own monthlySold field.
export function unitsSoldProvenance(signals?: AggregatedSignals | null): Provenance | null {
  if (!signals?.revenue?.value.est_monthly_units_sold) return null
  return estimated('Keepa', "Keepa's own monthlySold estimate for the category's top bestsellers — a real platform-side estimate, not a guess made up for this product.")
}

// "Estimated Monthly Revenue" / "Top Seller Revenue" / "Average Seller Revenue"
export function revenueEvidenceProvenance(signals?: AggregatedSignals | null): Provenance | null {
  if (!signals?.revenue) return null
  return estimated(
    'Keepa',
    "Real price × Keepa's own estimated units-sold, for the category's top bestsellers overall — not your specific product idea. monthlySold is itself Keepa's modeled estimate, not a measured fact, so this number carries that uncertainty forward."
  )
}

// "Category Avg Rating" / "Category Avg Review Count" — unlike est_monthly_units_sold
// and revenue above (Keepa's own modeled estimates), rating and review count are
// directly observed Amazon facts that Keepa mirrors via &rating=1, not something
// Keepa models — Verified, not Estimated. Same category-bestseller scope caveat
// as the rest of this signal, called out explicitly in the detail text.
export function categoryReviewDataProvenance(signals?: AggregatedSignals | null): Provenance | null {
  const rev = signals?.revenue?.value
  if (rev?.avg_rating === undefined && rev?.avg_review_count === undefined) return null
  return verified(
    'Keepa',
    "Real rating and review-count data, directly mirrored from Amazon (not modeled by Keepa), averaged across the category's top bestsellers — not specific to your exact product idea. Same category-wide scope as the revenue figures above."
  )
}

// "Competitor Count" / "Average Review Count" / "Market Concentration" — real
// only via a live Amazon search for this exact query (Apify's junglee/amazon-crawler
// actor — see providers/competition.ts). No fallback to market_saturation or any
// model-judgment substitute: a count either comes from counting real things,
// or it doesn't exist yet.
export function competitionEvidenceProvenance(signals?: AggregatedSignals | null): Provenance | null {
  const hasRealCompetitorData = signals?.review_velocity?.value.meaningful_competitor_count !== undefined
  if (!hasRealCompetitorData) return null
  return verified(
    signals!.review_velocity!.primarySource,
    'Review counts, ratings, and brand names are pulled directly from real organic Amazon search results for this exact query — no estimation step, no LLM involvement.'
  )
}

// Market Accessibility Score is a separate, clearly-derived SCORE (not a raw
// count) built on top of the competition evidence above. Scores are allowed
// a model-judgment fallback because a score is inherently a judgment call;
// counts are not.
export function marketAccessibilityProvenance(signals?: AggregatedSignals | null): Provenance {
  const hasRealCompetitorData = signals?.review_velocity?.value.meaningful_competitor_count !== undefined
  if (!hasRealCompetitorData) return synthesized('Computed from model judgment only — no real competitor/review data was available to ground this score.')
  return estimated(
    'Server-side formula',
    "Computed deterministically from the real review-count and review-concentration data above. The arithmetic is exact and server-enforced; the thresholds that decide what counts as 'accessible' are a judgment call encoded in the formula, not derived from market research."
  )
}

export function keywordIntelligenceProvenance(ki?: KeywordIntelligence | null): Provenance | null {
  if (!ki) return null
  return verified(
    'DataForSEO',
    "Search volume, competition, and CPC are pulled directly from DataForSEO's keyword database. Growth % is computed here from their real monthly search-volume history, not requested as a pre-built metric — the bucketing into Top Buying / Opportunity / Long-Tail / Fast-Growing is a deterministic sort/filter over those real numbers, not a model judgment."
  )
}

export function consumerIntelligenceProvenance(ci?: ConsumerIntelligenceReport | null): Provenance | null {
  if (!ci) return null
  return verified(
    'Apify (Amazon reviews)',
    `Every theme is a real, literal phrase pulled from ${ci.totalReviewsCollected} real customer reviews on ${ci.asinsAnalyzed.length} top competitor product(s) — no LLM summarization. Themes are found by deterministic phrase clustering (frequency + stemming, not free-form interpretation) and each one shows exactly how many of the analyzed reviews mentioned it. The review count next to every theme is the actual count, not an estimate.`
  )
}

// Per-dimension provenance for the Opportunity Score breakdown
// (lib/scoring.ts computeGroundedScore) — one badge per dimension, distinct
// from the single overall opportunityScoreProvenance below.
export function scoreDimensionProvenance(d: ScoreDimension): Provenance {
  return d.source === 'verified'
    ? verified(d.sourceLabel, `This dimension's score (${d.rawScore}/10) is read directly from real provider data, not written by the model.`)
    : estimated(d.sourceLabel, `This dimension's score (${d.rawScore}/10) is the model's own judgment — ${d.sourceLabel.toLowerCase()}.`)
}

// Overall score provenance — names what fraction of the score's weight is
// actually grounded, since the score itself is now a blend.
export function opportunityScoreProvenance(groundedPct: number): Provenance {
  if (groundedPct >= 50) {
    return estimated('Blended score', `${groundedPct}% of this score's weight comes from real provider data (Keepa/Apify/DataForSEO/TikTok); the rest is AI judgment, clearly marked below.`)
  }
  return synthesized(`Only ${groundedPct}% of this score's weight comes from real data — most of this number is AI judgment. Treat the BUILD_NOW/SKIP call with caution until more real signals are available.`)
}

// Consistency-check flags (lib/consistency.ts) — a claim that was checked
// against real evidence and contradicted it, or had none to point to.
export function consistencyFlagProvenance(flag: ConsistencyFlag): Provenance {
  return unsupported(`${flag.claim}. ${flag.evidence}`)
}

// ── Evidence Coverage % ──────────────────────────────────────────────────────
// One number answering "how much of THIS report is real data vs AI
// judgment?" — every distinct field/dimension a memo can show, classified
// by its actual provenance for this specific generation. A field that came
// back "No data available" counts as ungrounded here, same as a field that
// is always AI-only by nature — both genuinely mean less of this report is
// backed by real evidence, which is the whole point of the metric. Verified
// and estimated both count as "grounded" (estimated = real data was given
// to the model as required context, even though the model wrote the final
// text) — synthesized and unknown count as "not grounded."

export interface EvidenceCoverage {
  pct:              number   // 0-100
  groundedCount:    number   // verified + estimated
  totalCount:       number
  verifiedCount:    number
  estimatedCount:   number
  synthesizedCount: number
  unknownCount:     number
}

export function computeEvidenceCoverage(m: MemoData): EvidenceCoverage {
  const sig = m.signal_metadata
  const se  = m.signal_evidence

  const fields: Provenance[] = [
    // Dimension scores
    demandProvenance(sig),
    viralityProvenance(sig),
    subscriptionProvenance(),
    manufacturingScoreProvenance(),
    marketSaturationProvenance(sig),
    // Narrative + static fields (everything in STATIC_PROVENANCE except the
    // opportunity score, which has its own real per-dimension breakdown via
    // lib/scoring.ts rather than the single static entry here)
    STATIC_PROVENANCE.executiveSummary,
    STATIC_PROVENANCE.marketThesis,
    STATIC_PROVENANCE.whyNow,
    STATIC_PROVENANCE.buildExplanation,
    STATIC_PROVENANCE.marketGaps,
    STATIC_PROVENANCE.brandOpportunities,
    STATIC_PROVENANCE.customerLanguage,
    STATIC_PROVENANCE.productFormula,
    STATIC_PROVENANCE.productEconomics,
    STATIC_PROVENANCE.financialProjections,
    STATIC_PROVENANCE.marketSize,
    biggestCompetitorProvenance(sig),
    // Evidence-first layer — counted as ungrounded (via the synthesized
    // fallback) when the real source wasn't available for this query,
    // exactly like every other field above.
    searchVolumeProvenance(m.keyword_intelligence) ?? synthesized('No real search-volume data (DataForSEO) was found for this query.'),
    searchGrowthProvenance(se) ?? synthesized('No real search-growth data (Keepa/Google Trends) was found for this query.'),
    unitsSoldProvenance(se) ?? synthesized('No real units-sold data (Keepa) was found for this query.'),
    revenueEvidenceProvenance(se) ?? synthesized('No real revenue data (Keepa) was found for this query.'),
    categoryReviewDataProvenance(se) ?? synthesized('No real rating/review data (Keepa) was found for this query.'),
    competitionEvidenceProvenance(se) ?? synthesized('No real competitor search data (Apify) was found for this query.'),
    marketAccessibilityProvenance(se),
    keywordIntelligenceProvenance(m.keyword_intelligence) ?? synthesized('No real keyword data (DataForSEO) was found for this query.'),
    consumerIntelligenceProvenance(m.consumer_intelligence) ?? synthesized('No real consumer review data (Apify) was found for this query.'),
  ]

  const verifiedCount    = fields.filter(f => f.level === 'verified').length
  const estimatedCount   = fields.filter(f => f.level === 'estimated').length
  const synthesizedCount = fields.filter(f => f.level === 'synthesized').length
  const unknownCount     = fields.filter(f => f.level === 'unknown' || f.level === 'unsupported').length
  const groundedCount    = verifiedCount + estimatedCount
  const totalCount       = fields.length

  return {
    pct: totalCount > 0 ? Math.round((groundedCount / totalCount) * 100) : 0,
    groundedCount, totalCount, verifiedCount, estimatedCount, synthesizedCount, unknownCount,
  }
}
