// ═══════════════════════════════════════════════════════════════
// PROVENANCE — single source of truth for where every number, quote,
// and claim shown in a memo actually came from.
//
// Traced directly from the generation pipeline (app/api/generate/route.ts,
// lib/prompts/*, lib/categories/shared-prompts.ts, lib/signal-engine/*,
// lib/manufacturing-engine/*) — every classification below reflects what
// the code actually does, not what would be nice to claim.
//
// Four levels, matching how the data is actually produced:
//   verified    — pulled directly from a real external source, no LLM step
//   estimated   — real external data was retrieved AND given to the model
//                 as required grounding context, but the model still wrote
//                 the displayed score/notes text itself (compliance is a
//                 prompt instruction, not a server-enforced override)
//   synthesized — pure model output: pattern-matched from training
//                 knowledge, no external data involved at any stage
//   unknown     — provenance can't be determined (legacy/deprecated
//                 fields, failed-generation placeholders)
// ═══════════════════════════════════════════════════════════════

import type { MemoData, SignalMetadata } from '@/types/index'
import type { AggregatedSignals } from '@/lib/signal-engine/types'
import type { KeywordIntelligence } from '@/lib/keyword-engine/types'

export type ProvenanceLevel = 'verified' | 'estimated' | 'synthesized' | 'unknown'

export interface Provenance {
  level:  ProvenanceLevel
  source: string   // short attribution, e.g. "Keepa (Amazon)"
  detail: string   // one- or two-sentence explanation, used as a tooltip
}

const verified    = (source: string, detail: string): Provenance => ({ level: 'verified', source, detail })
const estimated    = (source: string, detail: string): Provenance => ({ level: 'estimated', source, detail })
const synthesized = (detail: string): Provenance => ({ level: 'synthesized', source: 'Claude (AI synthesis)', detail })
const unknown      = (detail: string): Provenance => ({ level: 'unknown', source: 'Unclassified', detail })

// ── Fields whose provenance is fixed regardless of which memo you're
// looking at — the generation pipeline never grounds these in real data. ──

